from math import sqrt

# Funciones de operaciones matemáticas básicas para probar en tests.

def sumar(x, y):
    return x + y

def restar(x, y):
    return x - y

def multiplicar(x, y):
    return x * y

def dividir(x, y):
    return x/y

def elevar(x, y):
    return x**y

def raiz_cuadrada(x):
    return sqrt(x)
